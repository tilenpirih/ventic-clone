  methods: {
    changeTheme(theme) {
      this.$store.state.Settings.Appearance = {
        ...this.$store.state.Settings.Appearance,
        ...theme.value.state,
      }
      this.$vuetify.theme.dark = theme.value.vuetify.dark
      Object.keys(theme.value.vuetify.themes.dark).forEach(key => {
        Vue.set(this.$vuetify.theme.themes.dark, key, theme.value.vuetify.themes.dark[key])
      })
      Object.keys(theme.value.vuetify.themes.light).forEach(key => {
        Vue.set(this.$vuetify.theme.themes.light, key, theme.value.vuetify.themes.light[key])
      })
