    selectedTheme() {
      const obj = {}
      obj.vuetify = {}
      obj.vuetify.themes = _.cloneDeep(this.$vuetify.theme.themes)
      obj.vuetify.dark = _.cloneDeep(this.$vuetify.theme.dark)
      obj.state = _.cloneDeep(this.$store.state.Settings.Appearance)
      delete obj.state.gridCardWidth
      delete obj.state.horizontalCardWidth
      return this.themes.findIndex(theme => _.isEqual(theme.value, obj))
    },
